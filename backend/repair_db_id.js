import { sequelize } from './src/models/index.js';

async function repair() {
  try {
    console.log('--- 🛠️ Database Repair Started ---');

    // 1. Drop existing default if it exists (Serial-style default)
    console.log('1. Clearing old defaults on "id" column...');
    await sequelize.query(`
      ALTER TABLE "users" ALTER COLUMN "id" DROP DEFAULT;
    `);

    // 2. Convert to Identity column
    // We use "BY DEFAULT" so manual inserts (if needed) are still possible, 
    // but the system will automatically handle it for Sequelize.
    console.log('2. Converting "id" to GENERATED BY DEFAULT AS IDENTITY...');
    await sequelize.query(`
      ALTER TABLE "users" ALTER COLUMN "id" ADD GENERATED BY DEFAULT AS IDENTITY;
    `);

    // 3. Synchronize the sequence with the current max ID
    console.log('3. Synchronizing internal counter with existing data...');
    const [[{ maxId }]] = await sequelize.query('SELECT MAX(id) as "maxId" FROM "users"');
    const startValue = (maxId || 0) + 1;
    
    await sequelize.query(`
      SELECT setval(pg_get_serial_sequence('users', 'id'), ${startValue}, false);
    `);

    console.log(`✅ Repair complete! Next ID will be: ${startValue}`);

    // 4. Verify table structure again
    const [columns] = await sequelize.query(`
      SELECT column_name, data_type, column_default, is_identity
      FROM information_schema.columns
      WHERE table_name = 'users' AND column_name = 'id';
    `);
    
    console.log('Updated Schema Details:', JSON.stringify(columns, null, 2));

  } catch (err) {
    console.error('❌ Repair failed:', err.message);
    if (err.message.includes('already is an identity column')) {
      console.log('💡 Note: Column is already an identity column. Let\'s try to just sync the sequence.');
      try {
        const [[{ maxId }]] = await sequelize.query('SELECT MAX(id) as "maxId" FROM "users"');
        const startValue = (maxId || 0) + 1;
        await sequelize.query(`SELECT setval(pg_get_serial_sequence('users', 'id'), ${startValue}, false);`);
        console.log(`✅ Sequence synchronized to: ${startValue}`);
      } catch (seqErr) {
        console.error('Failed to sync sequence:', seqErr.message);
      }
    }
  } finally {
    await sequelize.close();
  }
}

repair();
